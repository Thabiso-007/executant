        <div class="detail">
          <h3>Spider man</h3>
          <p>A strange black entity from another world bonds with Peter Parker and causes inner turmoil as he contends with new villains, temptations, and revenge. A strange black entity from another world bonds with Peter Parker and causes inner turmoil as he contends with new villains, temptations, and reveng</p>
        </div>
      </router-link>
    </div>
    <form class="search-box">
      <input type="text" placeholder="What are you looking for?" v-model="search" />
      <button @click="SearchMovies" type="button">Search</button>
    </form>
    <div class="movie-list">
      <div class="movie" v-for="movie in movies" :key="movie.imdbID">
        <router-link :to="'/movie/' + movie.imdbID" class="movie-link">
          <div class="product-image">
            <img :src="movie.Poster" alt="Movie Poster">
            <div class="type">{{ movie.Type }}</div>
          </div>
          <div class="detail">
            <p class="year">{{ movie.Year }}</p>
            <h3>{{ movie.Title }}</h3>
          </div>
        </router-link>
      </div>
    </div>
  </div>
</template> 

<script>
import { ref } from 'vue';
import env from '@/env';

export default {
  setup() {
    const search = ref("");
    const movies = ref([]);

    const SearchMovies = () => {
      if (search.value != "") {
        fetch(`http://www.omdbapi.com/?apikey=${env.apikey}&s=${search.value}`)
          .then(response => response.json())
          .then(data => {
            movies.value = data.Search;
            search.value = "";
          })          
      }
    }

    return {
      search,
      movies,
      SearchMovies
    }
  }
}
</script>

